public class ForLoop {
  public static void main(String[] args) {

    // Direct
    for (int i = 0; i < 10; i++) {
      System.out.println(i);
    }

    int start = 5;
    int end = 9;

    // using pre-saved variables
    for (int j = start; j < end; j++) {
      System.out.println(j);
    }
  }
}
