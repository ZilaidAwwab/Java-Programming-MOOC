public class WorkingWithArray {
  public static void main(String[] args) {
    
    // creating an array

    // We created an array to hold 3 integers
    int[] numbers = new int[3];

    // An array to hold 5 strings is declared as
    String[] strings = new String[5];

    // Assigning and accessing elements to arrays
    numbers[0] = 21;
    numbers[1] = 433;

    strings[0] = "Camilo";
    strings[1] = "Hamilton";
  }
}
