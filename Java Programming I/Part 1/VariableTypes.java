public class VariableTypes {
  public static void main(String[] args) {
    String text = "Contains text";
    int wholeNumber = 123;
    double floatingPoint = 3.576;
    boolean trueOrFalse = true;

    System.out.println("String variable: " + text);
    System.out.println("Int variable: " + wholeNumber);
    System.out.println("Double variable: " + floatingPoint);
    System.out.println("Boolean variable: " + trueOrFalse);
  }
}
